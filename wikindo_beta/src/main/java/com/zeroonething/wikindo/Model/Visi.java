package com.zeroonething.wikindo.Model;

public class Visi {

    private String noVIsi;
    private String Visi;

    public Visi(String noVIsi, String Visi) {
        this.noVIsi = noVIsi;
        this.Visi = Visi;
    }

    public String getNoVIsi() {
        return noVIsi;
    }

    public void setNoVIsi(String noVIsi) {
        this.noVIsi = noVIsi;
    }

    public String getVisi() {
        return Visi;
    }

    public void setVisi(String Visi) {
        this.Visi = Visi;
    }

}
